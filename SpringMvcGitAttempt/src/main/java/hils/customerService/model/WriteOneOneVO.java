package hils.customerService.model;

public class WriteOneOneVO {
	private String userEmail;
	private String userSubject;
	private String textToManager;
	
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getUserSubject() {
		return userSubject;
	}
	public void setUserSubject(String userSubject) {
		this.userSubject = userSubject;
	}
	public String getTextToManager() {
		return textToManager;
	}
	public void setTextToManager(String textToManager) {
		this.textToManager = textToManager;
	}
	
}
