package hils.timeLine1.model;

public class WorkoutCalendarOnedayDto {
	private String workout_key;
	private String workout_name;
	private String workout_hours;
	public String getWorkout_key() {
		return workout_key;
	}
	public void setWorkout_key(String workout_key) {
		this.workout_key = workout_key;
	}
	public String getWorkout_name() {
		return workout_name;
	}
	public void setWorkout_name(String workout_name) {
		this.workout_name = workout_name;
	}
	public String getWorkout_hours() {
		return workout_hours;
	}
	public void setWorkout_hours(String workout_hours) {
		this.workout_hours = workout_hours;
	}
	
}
